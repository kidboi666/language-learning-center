package practice.variable;

public class Var7 {

  public static void main(String[] args) {
    int a = 100; //정수
    double b = 3.14; //실수
    boolean c = true; //불리언
    char d = 'a'; //문자 하나
    String e = "Hello"; //문자열을 다루기 위한 특별한 타입

    byte f = 127; //-128 ~ 127
    short g = 128; //-32,768 ~ 32,767
    int h = 128; //-2,147,483,648 ~ 2,147,483,647
    long i = 128; // -9,223,372,036,854,775,808 ~ 9,223,372,036,854,775,807
    float f1 = 128;
    double d1 = 128;

    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
  }

}
